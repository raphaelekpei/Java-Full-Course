package operators;

public class DecrementOperator {

    public static void main(String[] args) {

        int number = 0;

        System.out.println(number--);
        System.out.println(number);

        System.out.println();

        int numberTwo = 0;
        System.out.println(--numberTwo);
        System.out.println(numberTwo);
    }
}
