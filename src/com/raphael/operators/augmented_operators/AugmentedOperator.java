package com.raphael.operators.augmented_operators;

public class AugmentedOperator {
    public static void main(String[] args) {

        int numberOne = 10;
        numberOne += 2;
        System.out.println(numberOne);

        int numberTwo = 10;
        numberTwo -= 2;
        System.out.println(numberTwo);

        int numberThree = 10;
        numberThree *= 2;
        System.out.println(numberThree);

        int numberFour = 10;
        numberFour /= 2;
        System.out.println(numberFour);

        int numberFive = 10;
        numberFive %= 2;
        System.out.println(numberFive);


    }
}
